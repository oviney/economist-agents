#!/usr/bin/env python3
"""Continuous burn-down coordinator for backlog and remediation work.

The coordinator does four things in a repeatable loop:
1. Refresh local backlog sources and optional GitHub issue state
2. Run deterministic validators and convert failures into explicit todos
3. Separate actionable work from real blockers
4. Optionally claim the next GitHub issue for a runtime

This is designed as a bounded, testable loop rather than an unbounded daemon.
Use ``--max-iterations`` with ``--interval-seconds`` if you want it to keep
refreshing until queues are clear or blocked.
"""

